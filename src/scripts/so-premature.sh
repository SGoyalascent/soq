Donald Knuth's 1974 paper [Structured Programming with go to
Statements](https://citeseerx.ist.psu.edu/viewdoc/summary?doi=10.1.1.103.6084)
says:

_"Programmers waste enormous amounts of time thinking about, or worrying
about, the speed of noncritical parts of their programs, and these
attempts at efficiency actually have a strong negative impact when
debugging and maintenance are considered.
We should forget about small efficiencies, say about 97% of the time:
premature optimization is the root of all evil.
Yet we should not pass up our opportunities in that critical 3%."_

